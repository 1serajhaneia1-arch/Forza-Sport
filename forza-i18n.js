  function clean(text) {
    return (text || '').replace(/[^\p{L}\p{N}\s&·?]/gu, '').replace(/\s+/g, ' ').trim();
  }

  function translateTextNodes(lang) {
    if (lang !== 'ar') return;
    var map = dict.ar;
    var walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
      acceptNode: function (node) {
        if (!node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
        if (node.parentElement && ['SCRIPT', 'STYLE', 'TEXTAREA'].includes(node.parentElement.tagName)) return NodeFilter.FILTER_REJECT;
        return NodeFilter.FILTER_ACCEPT;
      }
    });
    var nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);
    nodes.forEach(function (node) {
      var original = node.nodeValue;
      var key = clean(original);
      if (map[key]) node.nodeValue = original.replace(key, map[key]);
    });
  }

  function translatePlaceholders(lang) {
    if (lang !== 'ar') return;
    var map = placeholderDict.ar;
    document.querySelectorAll('[placeholder]').forEach(function (el) {
      var val = el.getAttribute('placeholder');
      if (map[val]) el.setAttribute('placeholder', map[val]);
    });
  }

  function applyLang(lang) {
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.body.classList.toggle('rtl', lang === 'ar');
    localStorage.setItem(KEY, lang);
    translateTextNodes(lang);
    translatePlaceholders(lang);
    var btn = document.getElementById('lang-toggle');
    if (btn) btn.textContent = lang === 'ar' ? 'EN' : 'AR';
  }

  function installToggle() {
    if (document.getElementById('lang-toggle')) return;
    var nav = document.querySelector('.nav-links') || document.querySelector('.topbar-right') || document.body;
    var wrap = document.createElement(nav.tagName === 'UL' ? 'li' : 'div');
    wrap.className = 'nav-li-auth lang-toggle-wrap';
    var btn = document.createElement('button');
    btn.id = 'lang-toggle';
    btn.type = 'button';
    btn.className = 'nav-btn-signin';
    btn.style.border = '1px solid rgba(255,255,255,0.18)';
    btn.style.background = 'transparent';
    btn.style.cursor = 'pointer';
    btn.onclick = function () {
      var next = (localStorage.getItem(KEY) || 'en') === 'ar' ? 'en' : 'ar';
      localStorage.setItem(KEY, next);
      location.reload();
    };
    wrap.appendChild(btn);
    nav.appendChild(wrap);
  }

  document.addEventListener('DOMContentLoaded', function () {
    installToggle();
    applyLang(localStorage.getItem(KEY) || 'en');
  });
